let day = "Tuesday";

switch (day) {
  case "Monday":
    console.log("Start of the week!");
    break;
  case "Tuesday":
    console.log("Second day of the week!");
    break;
  case "Wednesday":
    console.log("Mid-week already.");
    break;
  default:
    console.log("Not sure which day it is.");
}
// Output: Second day of the week!
