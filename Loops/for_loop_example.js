for (let i = 0; i < 5; i++) {
  console.log("Iteration number: " + i);
}
// Output:
// Iteration number: 0
// Iteration number: 1
// Iteration number: 2
// Iteration number: 3
// Iteration number: 4

// let i = 0: Initializes the counter i.
// i < 5: Condition to continue the loop.
// i++: Increment i by 1 after each iteration.
