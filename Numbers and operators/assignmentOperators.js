// Assignment Operators Example
let a = 5;

a += 3; // Equivalent to a = a + 3
console.log("Value of a (after += 3): " + a); // Output: 8

a -= 2; // Equivalent to a = a - 2
console.log("Value of a (after -= 2): " + a); // Output: 6

a *= 4; // Equivalent to a = a * 4
console.log("Value of a (after *= 4): " + a); // Output: 24

a /= 2; // Equivalent to a = a / 2
console.log("Value of a (after /= 2): " + a); // Output: 12

a **= 2; // Equivalent to a = a^2
console.log("Value of a (after **= 2): " + a); // Output: 144
