// Comparison Operators Example
let num1 = 10;
let num2 = 15;

console.log(num1 == num2); // Output: false
console.log(num1 < num2); // Output: true
console.log(num1 > num2); // Output: false
console.log(num1 != num2); // Output: true
console.log(num1 <= num2); // Output: true
console.log(num1 >= num2); // Output: false

console.log(num1 === 10); // Output: true (checks both value and type)
console.log(num1 !== "10"); // Output: true (strict inequality)
